public class MathOps {

    public static int add(int val1, int val2){
        return val1+val2;
    }

    public static int sub(int val1, int val2){
        return val1- val2;
    }
    public static int div(int val1, int val2){
        return val1/ val2;
    }
    public static int mul(int val1, int val2){
        return val1*val2;
    }
}
